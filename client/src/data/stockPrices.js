// Get stocks grouped by price range
export function getStocksByPriceRange() {
  const grouped = {}
  
  PRICE_RANGES.forEach(range => {
    grouped[range.id] = {
      ...range,
      stocks: []
    }
  })
  
  Object.entries(STOCK_PRICES).forEach(([code, price]) => {
    if (price === 0 || price === null || price === undefined) return
    
    for (const range of PRICE_RANGES) {
      if (price >= range.min && price < range.max) {
        grouped[range.id].stocks.push({ code, price })
        break
      }
    }
  })
  
  // Sort stocks by price within each group
  Object.keys(grouped).forEach(rangeId => {
    grouped[rangeId].stocks.sort((a, b) => a.price - b.price)
  })
  
  return grouped
}

// Get all stock codes
export function getAllStockCodes() {
  return Object.keys(STOCK_PRICES).filter(code => STOCK_PRICES[code] > 0)
}

// Get stock price
export function getStockPrice(code) {
  return STOCK_PRICES[code.toUpperCase()] || null
}

// Search stocks
export function searchStocks(query) {
  const term = query.toUpperCase()
  return Object.entries(STOCK_PRICES)
    .filter(([code, price]) => code.includes(term) && price > 0)
    .map(([code, price]) => ({ code, price }))
    .sort((a, b) => a.code.localeCompare(b.code))
}

// Get stocks count by range
export function getStocksCountByRange() {
  const grouped = getStocksByPriceRange()
  return PRICE_RANGES.map(range => ({
    ...range,
    count: grouped[range.id].stocks.length
  }))
}

